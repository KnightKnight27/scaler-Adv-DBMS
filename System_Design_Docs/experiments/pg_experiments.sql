-- PostgreSQL experiments (run on PostgreSQL 17.9)
--   createdb adbms_exp && psql -d adbms_exp -f pg_experiments.sql
-- Covers: query planning, MVCC tuple versioning, buffer manager, B-tree, WAL.

CREATE EXTENSION IF NOT EXISTS pageinspect;
CREATE EXTENSION IF NOT EXISTS pg_buffercache;

DROP TABLE IF EXISTS orders, customers;
CREATE TABLE customers (id int PRIMARY KEY, name text, city text, signup_date date);
CREATE TABLE orders (id int PRIMARY KEY, customer_id int REFERENCES customers(id),
                     amount numeric(10,2), status text, created_at timestamp);

SELECT setseed(0.42);
INSERT INTO customers SELECT g, 'Customer_'||g,
       (ARRAY['Mumbai','Delhi','Bengaluru','Chennai','Kolkata','Pune'])[1+floor(random()*6)::int],
       date '2023-01-01' + (floor(random()*500))::int
FROM generate_series(1,10000) g;
INSERT INTO orders SELECT g, 1+floor(random()*10000)::int, (random()*1000)::numeric(10,2),
       (ARRAY['paid','pending','shipped','cancelled'])[1+floor(random()*4)::int],
       timestamp '2024-01-01' + (floor(random()*200000))::int * interval '1 minute'
FROM generate_series(1,200000) g;
CREATE INDEX idx_orders_customer ON orders(customer_id);
CREATE INDEX idx_orders_status   ON orders(status);
VACUUM ANALYZE;

-- 1. Cost-based planning: index scan vs forced seq scan, and a multi-table join.
EXPLAIN (ANALYZE, BUFFERS) SELECT * FROM orders WHERE id = 12345;
EXPLAIN (ANALYZE, BUFFERS) SELECT * FROM orders WHERE customer_id = 4242;
EXPLAIN (ANALYZE, BUFFERS) SELECT c.city, count(*), round(avg(o.amount),2)
  FROM customers c JOIN orders o ON o.customer_id=c.id
  WHERE c.city='Mumbai' AND o.status='paid' GROUP BY c.city;
SET enable_indexscan=off; SET enable_bitmapscan=off;
EXPLAIN (ANALYZE) SELECT * FROM orders WHERE id = 12345;   -- forced seq scan for cost contrast
RESET enable_indexscan; RESET enable_bitmapscan;
SELECT attname, n_distinct, correlation FROM pg_stats
  WHERE tablename='orders' AND attname IN ('status','customer_id','id');

-- 2. MVCC: UPDATE writes a new tuple version (xmin/xmax/ctid version chain).
CREATE TABLE mvcc_demo (id int PRIMARY KEY, balance int);
INSERT INTO mvcc_demo VALUES (1, 100);
SELECT ctid, xmin, xmax, balance FROM mvcc_demo;
UPDATE mvcc_demo SET balance=200 WHERE id=1;
UPDATE mvcc_demo SET balance=300 WHERE id=1;
UPDATE mvcc_demo SET balance=400 WHERE id=1;
SELECT t_ctid AS points_to, lp, t_xmin, t_xmax FROM heap_page_items(get_raw_page('mvcc_demo',0));
VACUUM mvcc_demo;   -- reclaims dead versions

-- 3. Buffer manager: what is cached in shared_buffers.
SELECT c.relname, count(*) AS buffers, pg_size_pretty(count(*)*8192) AS cached
FROM pg_buffercache b JOIN pg_class c ON b.relfilenode = pg_relation_filenode(c.oid)
WHERE c.relname IN ('orders','customers','orders_pkey','idx_orders_status','idx_orders_customer')
GROUP BY c.relname ORDER BY buffers DESC;

-- 4. B-tree structure (height/levels) and on-disk sizes.
SELECT * FROM bt_metap('orders_pkey');
SELECT relname, pg_size_pretty(pg_relation_size(oid)) heap,
       pg_size_pretty(pg_indexes_size(oid)) indexes
FROM pg_class WHERE relname IN ('orders','customers') AND relkind='r';

-- 5. WAL: bytes generated by a write (written before data pages = durability).
SELECT pg_current_wal_lsn() AS lsn_before \gset
INSERT INTO orders SELECT 200000+g, 1+(g%10000), 50, 'paid', now() FROM generate_series(1,10000) g;
SELECT pg_current_wal_lsn() AS lsn_after \gset
SELECT pg_size_pretty(pg_wal_lsn_diff(:'lsn_after', :'lsn_before')) AS wal_for_10k_inserts;
